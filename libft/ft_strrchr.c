#include "libft.h"

int	ft_strrchr(char *s, int c)
{
	int	j;

	j = 0;
	while (s[j])
		j++;
	while (j >= 0)
	{
		if (s[j] == (unsigned char)c)
			return (j);
		j--;
	}
	return (-1);
}
